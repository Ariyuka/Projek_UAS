    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here: try
        try
        {
            Statement stat = koneks.createStatement(); //membuat variabel untuk statement
            //mengeksekusi query sql
            ResultSet data = stat.executeQuery("SELECT * FROM tamu WHERE id_tamu='" + jTextField1.getText() + "'");
            if (data.next())
            {
                //menampilkan pesan bahwa data id menu sudah ada;
                JOptionPane.showMessageDialog (null, "ID Tamu sudah ada!!!", "PERHATIAN", JOptionPane.WARNING_MESSAGE);
                jTextField1.requestFocus(); //menempatkan kursor di idmenu[jTextField]
            }
            else if (jTextField1.getText().equals("") || jTextField2.getText().equals("") ||
                jTextField3.getText().equals("") || jTextField4.getText().equals("")) //jika isian jTextField kosong
            {
                //menampilkan pesan bahwa isian semua jTextField tIdak boleh kosong
                JOptionPane.showMessageDialog (null, "Data Tidak Boleh Kosong!!!", "PERHATIAN", JOptionPane.WARNING_MESSAGE);
            }
            else
            {
                //query sql untuk insert data
                String sql = "INSERT INTO tamu VALUES ('" + jTextField1.getText() + "'"
                + ",'" + jTextField2.getText() + "'"
                + ",'" + jTextField3.getText() + "'"
                + ",'" + jTextField4.getText() + "')";
                stat.executeUpdate(sql); //mengeksekusi query sql
                //menghapusdatainputandiform
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField1.requestFocus(); //menempatkan kursor di idmenu[jTextField]
                //menampilkan pesan bahwa data berhasil disimpan
                JOptionPane.showMessageDialog (null, "Data Berhasil DiSimpan!!!", "SUKSES", JOptionPane.INFORMATION_MESSAGE);
                tampilData(); //memanggil method tampil data
            }
            stat.close();//menutup statement
        }
        catch (Exception exc)
        {
            System.err.println("Terjadi Kesalahan :" + exc);
        }
        
    
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin mengubah data ini?", "Konfirmasi Dialog", JOptionPane.YES_NO_OPTION);
        if (ok == 0)
        {
            try
            {
                //query sql untuk update data
                String sql ="UPDATE tamu SET nama_tamu = '"+jTextField2.getText()
                        +"', tipe_kamar = '"+jTextField3.getText()
                        +"', tarif = '"+jTextField4.getText()
                        +"' WHERE id_tamu='" + jTextField1.getText().trim() + "'";
                
                try (Statement stat = koneks.createStatement()) {
                    stat.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data berhasil diubah!", "Ubah Data", JOptionPane.INFORMATION_MESSAGE);
                }
                
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField1.enable(true);
                jTextField1.requestFocus();
                tampilData();
            }
            catch (HeadlessException | SQLException e)
            {
                JOptionPane.showMessageDialog(null, "Perubahan data Gagal"+e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

