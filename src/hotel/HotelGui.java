    public void tampilData(){
        DefaultTableModel tamu = new DefaultTableModel();
        tamu.addColumn("ID Tamu");
        tamu.addColumn("Nama Tamu");
        tamu.addColumn("Tipe Kamar");
        tamu.addColumn("Tarif");
        //atur jTable dengan nama kolom di atas
       jTable1.setModel(tamu);
        
        //menampilkan data dari database kedalam tabel
        try {
            Statement stat = koneks.createStatement (); //membuat variabel untuk statement
            ResultSet data = stat.executeQuery("SELECT * FROM tamu"); //mengeksekusi query
            while (data.next())  {
                //menambahkan baris dimodel sesuai dengan data dari tabel di database
                tamu.addRow (new Object[]   {
                    data.getString("id_tamu"),
                    data.getString("nama_tamu"),
                    data.getString("tipe_kamar"),
                    data.getString("tarif"),
                });
                //atur jTable dengan data dari model
                jTable1.setModel(tamu);
                }
        } catch (SQLException e) {
            System.err.println("Terjadi Kesalahan :" + e);
        }
        
    }

